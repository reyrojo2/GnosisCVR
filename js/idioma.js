function cambiarIdioma() {
    const idioma = document.querySelector("#selector-idioma").value;
    const textos = traducciones[idioma];
    const claves = Object.keys(textos);

    //Recorre las claves y actualiza el content de cada elemnto
    for (let i = 0; i < claves.length; i++) {
        const id = claves[i];
        const elemento = document.querySelector(id);
        //Si existe el elemento, actualiza su content
        if (elemento) {
            elemento.innerHTML = textos[id];
        }
    }

}

let carruselActivo = null;

function carruselFrases() {
    if (carruselActivo) {
        clearTimeout(carruselActivo);
        carruselActivo = null;
    }

    const fraseElemento = document.getElementById('p-frase');
    if (!fraseElemento) return;

    const idioma = document.querySelector("#selector-idioma").value;
    const frases = traducciones[idioma]?.frases;
    if (!frases) return;

    let i = 0;
    function actualizarCarrusel() {
        fraseElemento.style.opacity = 0;
        setTimeout(() => {
            fraseElemento.textContent = frases[i];
            fraseElemento.style.opacity = 1;
            i = (i + 1) % frases.length;
        }, 500);

        carruselActivo = setTimeout(actualizarCarrusel, 3000);
        console.log("instancia carrusel");
    }
    actualizarCarrusel();
}

document.addEventListener('DOMContentLoaded', function () {
    const selector = document.getElementById("selector-idioma");

    // Aplicar idioma guardado (si existe)
    const idiomaGuardado = localStorage.getItem("idioma");
    if (idiomaGuardado) {
        selector.value = idiomaGuardado;
        cambiarIdioma();
    }

    selector.addEventListener('change', () => {
        localStorage.setItem("idioma", selector.value);  // Guardar idioma
        cambiarIdioma();
        carruselFrases();
    });

    carruselFrases();
});
